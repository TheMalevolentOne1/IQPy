'''
Write a program that:
• asks the user their name
• if it is the same as your name, outputs
'You’re cool', otherwise outputs 'Nice to meet you'

Variables
If/Else Statement
'''
name = input("Whats your name?").lower()
if name == "akos":
    print("You're cool")
else:
    print("Nice to meet you")
# asks user for name, using if/else statements compares it to 'akos' and checks name for the same name
